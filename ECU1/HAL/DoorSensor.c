/**********************************************************************************************************************
 *  INCLUDES
 *********************************************************************************************************************/
#include "DoorSensor.h"

/**********************************************************************************************************************
 *  GLOBAL FUNCTIONS
 *********************************************************************************************************************/

/******************************************************************************
* \Syntax          : void init()
* \Description     : initialize Speed Sensor
*******************************************************************************/
 void initDoorSensor()
{
	GPIO_init(DoorSensorPort,DoorSensorPin,INPUT);
}

/******************************************************************************
* \Syntax          : doorState_t getState()
* \Description     : get Current State of the sensor
*******************************************************************************/
doorState_t getDoorState()
{
	return GPIO_readPin(DoorSensorPort,DoorSensorPin);
}

