/**********************************************************************************************************************
 *  INCLUDES
 *********************************************************************************************************************/
#include "SpeedSensor.h"

/**********************************************************************************************************************
 *  GLOBAL FUNCTIONS
 *********************************************************************************************************************/

/******************************************************************************
* \Syntax          : void init()
* \Description     : initialize Speed Sensor
*******************************************************************************/
 void initSpeedSensor()
{
	GPIO_init(SpeedSensorPort,SpeedSensorPin,INPUT);
}

/******************************************************************************
* \Syntax          : SpeedState_t getState()
* \Description     : get Current State of the sensor
*******************************************************************************/
 SpeedState_t getSpeedState()
{
	return GPIO_readPin(SpeedSensorPort,SpeedSensorPin);
}

