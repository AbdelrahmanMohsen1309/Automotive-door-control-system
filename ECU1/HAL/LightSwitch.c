/**********************************************************************************************************************
 *  INCLUDES
 *********************************************************************************************************************/
#include "LightSwitch.h"

/**********************************************************************************************************************
 *  GLOBAL FUNCTIONS
 *********************************************************************************************************************/

/******************************************************************************
* \Syntax          : void init()
* \Description     : initialize Speed Sensor
*******************************************************************************/
 void initLightSwitch()
{
	GPIO_init(LightSwitchPort,LightSwitchPin,INPUT);
}

/******************************************************************************
* \Syntax          : LightSwitchState_t getState()
* \Description     : get Current State of the switch
*******************************************************************************/
LightSwitchState_t getSwitchState()
{
	return GPIO_readPin(LightSwitchPort,LightSwitchPin);
}
